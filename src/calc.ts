export function sum(a: number, b: number) {
  return a + b;
}
export function subt(a: number, b: number) {
  return a - b;
}
export function mult(a: number, b: number) {
  return a * b;
}
export function divi(a: number, b: number) {
  return a / b;
}

export function perc(a: number, b: number) {
  return a % b;
}
